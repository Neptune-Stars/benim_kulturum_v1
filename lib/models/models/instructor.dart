class Instructor {
  final int id;
  final String name;
  final String department;
  final String office;
  final String title;
  final String filter;
  final String email;

  Instructor({
    required this.id,
    required this.name,
    required this.department,
    required this.office,
    required this.title,
    required this.filter,
    required this.email,
  });
}